def parse_era(text):
    """从文本中提取年号和年份"""
    for era, start in ERA_YEAR.items():
        # "建安五年"
        m = re.search(rf'{era}(\d+)年', text)
        if m:
            year = start + int(m.group(1)) - 1
            return year, f'{era}{m.group(1)}年（{year}年）'
        # "建安年间"
        if f'{era}年间' in text or f'{era}中' in text:
            return start, f'{era}年间'
    return None, None

def get_person_life(name):
    """获取人物活跃期"""
    name = name.strip()
    if name in PERSON_BD:
        return PERSON_BD[name]
    for k in PERSON_BD:
        if k in name or name in k:
            return PERSON_BD[k]
    return None

def get_person_origin(name):
    """获取人物籍贯"""
    name = name.strip()
    if name in PERSON_ORIGIN:
        return PERSON_ORIGIN[name]
    for k in PERSON_ORIGIN:
        if k in name:
            return PERSON_ORIGIN[k]
    return None

def extract_location(text):
    """从文本提取真实东汉地名"""
    # 郡县名模式
    patterns = [
        r'[在于至屯驻守攻战袭据奔走逃还入出到往过经渡临徙居于向趋赴](?:了|至|往|于|在|到)?(?:荆州|扬州|徐州|兖州|豫州|青州|冀州|并州|凉州|益州|幽州|交州|司隶)[^\s，。、；\n]{0,4}',
        r'[在于至屯驻守攻战袭据奔走逃还入出到往过经渡临徙居于向趋赴](?:了|至|往|于|在|到)?(?:郡|县|州)[^\s，。、；\n]{0,6}',
        r'[在于至屯驻守攻战袭据奔走逃还入出到往过经渡临徙居于向趋赴](?:了|至|往|于|在|到)?(?:寿春|下邳|小沛|彭城|合肥|赤壁|宛城|襄阳|樊城|江陵|公安|麦城|长坂|博望|邺城|许都|许昌|成都|汉中|江夏|南郡|江东|吴郡|会稽|丹阳|柴桑|建业|濡须|公安|夷陵|梓潼|永安|白帝|江州)[^\s，。、；\n]{0,4}',
    ]
    locs = []
    for pat in patterns:
        ms = re.findall(pat, text)
        for m in ms:
            m = m.strip('的了至往于在到')
            if len(m) >= 2 and len(m) <= 8:
                locs.append(m)
    if locs:
        seen = set()
        result = []
        for loc in locs:
            if loc not in seen:
                seen.add(loc)
                result.append(loc)
        return '、'.join(result[:3])
    return None

def infer_time(ev, title, brief, people):
    """推断时间"""
    text = title + ' ' + brief
    # 1. 从文本提取年号
    year, time_str = parse_era(text)
    if year:
        return year, time_str
    # 2. 从标题提取
    year, time_str = parse_era(title)
    if year:
        return year, time_str
    # 3. 知名事件锚点
    if '赤壁' in text:
        return 208, '建安十三年（208年）'
    if '官渡' in text:
        return 200, '建安五年（200年）'
    if '衣带诏' in text:
        return 199, '建安四年（199年）'
    if '刺董' in text or '董卓之死' in text:
        return 192, '初平三年（192年）'
    if '黄巾' in text:
        return 184, '中平元年（184年）'
    if '白门楼' in text:
        return 198, '建安三年（198年）'
    if '刘备入蜀' in text or '刘备定蜀' in text:
        return 214, '建安十九年（214年）'
    if '夷陵之战' in text or '火烧连营' in text:
        return 222, '章武二年（222年）'
    if '七擒孟获' in text:
        return 225, '建兴三年（225年）'
    if '出祁山' in text or '北伐' in text:
        return 228, '建兴六年（228年）'
    # 4. 人物卒年锚点
    main_person = people[0] if people else None
    if main_person:
        bd = get_person_life(main_person)
        if bd:
            if '去世' in title or '逝世' in title or '薨' in title or '卒' in title:
                return bd[1], f'约{bd[0]}-{bd[1]}年'
            elif '出生' in title or '诞生' in title:
                return bd[0], f'约{bd[0]}-{bd[1]}年'
            else:
                # 取活跃期中值
                mid = (bd[0] + bd[1]) // 2
                return mid, f'约{bd[0]}-{bd[1]}年'
    return None, None

def infer_location(ev, title, brief, people):
    """推断地点"""
    # 1. 从文本提取
    text = title + ' ' + brief
    loc = extract_location(text)
    if loc:
        return loc
    # 2. 从人物籍贯
    for p in people:
        origin = get_person_origin(p)
        if origin:
            return origin
    # 3. 事件锚点地点
    if '赤壁' in text:
        return '荆州江夏郡'
    if '官渡' in text:
        return '豫州陈留郡酸枣县'
    if '合肥' in text:
        return '扬州九江郡合肥县'
    if '荆州' in text:
        return '荆州南郡'
    if '扬州' in text:
        return '扬州吴郡'
    if '蜀' in text and '汉' not in text:
        return '益州蜀郡成都县'
    if '吴' in text and '孙' in text:
        return '扬州吴郡'
    return None

def fix_entry(ev, idx, name):
    title = ev.get('事件标题', '')
    brief = ev.get('事件简介', '')
    people = ev.get('相关人物', [])

    t = ev.get('时间', '')
    l = ev.get('地点', '')
    s = ev.get('std_start_year')

    new_time, new_loc, new_std = None, None, None

    if t == '不详' or t is None or t == '':
        year, time_str = infer_time(ev, title, brief, people)
        if time_str:
            new_time = time_str
        if year:
            new_std = year

    if l == '不详' or l is None or l == '':
        loc = infer_location(ev, title, brief, people)
        if loc:
            new_loc = loc

    if s is None and new_std is None:
        # 尝试从已知时间反推std
        if t != '不详' and t:
            year, _ = parse_era(t)
            if not year:
                # 尝试提取括号中的年份
                m = re.search(r'（(\d+)年）', t)
                if m:
                    new_std = int(m.group(1))

    return new_time, new_loc, new_std

def main():
    all_fixes = []
    bad_files = ['关羽_events.json', '刘禅_events.json', '常林_events.json']

    for fpath in sorted(glob.glob('*_events.json')):
        if fpath in bad_files:
            continue
        try:
            events = json.load(open(fpath, encoding='utf-8'))
        except:
            continue

        name = fpath.replace('_events.json', '')
        for idx, ev in enumerate(events):
            t = ev.get('时间', '')
            l = ev.get('地点', '')
            s = ev.get('std_start_year')

            needs_fix = (t == '不详') or (l == '不详') or (s is None)

            if not needs_fix:
                continue

            new_time, new_loc, new_std = fix_entry(ev, idx, name)

            fix = {'file': fpath, 'idx': idx, 'title': ev.get('事件标题', '')}

            changes = {}
            if new_time and t == '不详':
                changes['时间'] = new_time
                ev['时间'] = new_time
            if new_std and s is None:
                changes['std_start_year'] = new_std
                ev['std_start_year'] = new_std
            if new_loc and l == '不详':
                changes['地点'] = new_loc
                ev['地点'] = new_loc

            if changes:
                fix['补全'] = changes
                all_fixes.append(fix)

        # 写回文件
        with open(fpath, 'w', encoding='utf-8') as fp:
            json.dump(events, fp, ensure_ascii=False, indent=2)

    print(f'总共处理了 {len(all_fixes)} 条修复')
    # 输出JSON
    print(json.dumps(all_fixes, ensure_ascii=False, indent=2))

if __name__ == '__main__':
    main()
